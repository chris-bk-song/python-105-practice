# Write a function named "repeat"
# It should accept 2 arguments:
# a number and a string
# It should print out the string however many times you specify
# repeat(3 'good morning')
# good morning!
# good morning!
# good morning!

def repeat (num, string):
    return num * string

printout = repeat(3, 'good morning!' '\n')

print(printout)
