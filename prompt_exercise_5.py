# Access and replace multiple items in a list at a time

groceries = ['Milk', 'Bread', 'Beer', 'Cheese', 'Churos']
print(groceries[0:2]) # slicing starting from index 0 and ends at index 2
print(groceries[1:2]) # slicing starting from index 1 and ends at index 2
print(groceries[:]) # omit start and end number and just put colon = list all
